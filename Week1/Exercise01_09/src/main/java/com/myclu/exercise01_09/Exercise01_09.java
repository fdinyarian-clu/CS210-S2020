package com.myclu.exercise01_09;

/**
 * <p>
 *  (Area and perimeter of a rectangle) Write a program that displays the area and
 *  perimeter of a rectangle with the width of 4.5 and height of 7.9 using the following
 *  formula:
 *          area = width * height
 * 
 * @author      Farzin Dinyarian
 * @version     1.0
 * @since       12/22/2019
 */
public class Exercise01_09 {
    /**
    * main function to print area.
    * @param args 
    * @see       
    * @since     12/22/2019
    */  
    public static void main(String[] args) {
    // Display area
    System.out.println(4.5 * 7.9);

    // Display perimeter
    System.out.println(2 * (4.5 + 7.9));
  }
}
